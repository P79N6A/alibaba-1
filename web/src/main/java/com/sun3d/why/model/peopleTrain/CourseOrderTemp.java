	private String [] courseId;
	private String [] courseType;
	private String [] courseTitle;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String[] getCourseId() {
		return courseId;
	}
