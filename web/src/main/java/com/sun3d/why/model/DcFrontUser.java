package com.sun3d.why.model;

public class DcFrontUser {
    private String userId;

    private String userName;

    private String userPwd;

    private String userArea;

    private String areaOfficer;
    
    private String areaActivityCenter;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserPwd() {
        return userPwd;
    }

    public void setUserPwd(String userPwd) {
        this.userPwd = userPwd;
    }

    public String getUserArea() {
        return userArea;
    }

    public void setUserArea(String userArea) {
        this.userArea = userArea;
    }

    public String getAreaOfficer() {
        return areaOfficer;
    }

    public void setAreaOfficer(String areaOfficer) {
        this.areaOfficer = areaOfficer;
    }

	public String getAreaActivityCenter() {
		return areaActivityCenter;
	}

	public void setAreaActivityCenter(String areaActivityCenter) {
		this.areaActivityCenter = areaActivityCenter;
	}
    
    
}