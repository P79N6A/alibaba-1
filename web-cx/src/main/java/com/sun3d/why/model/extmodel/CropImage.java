package com.sun3d.why.model.extmodel;

public class CropImage {
	private String url;
	private String initWidth;
	private String initHeight;
	private String imgWidth;
	private String x;
	private String y;
	private String cropWidth;
	private String cropHeight;
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getInitWidth() {
		return initWidth;
	}
	public void setInitWidth(String initWidth) {
		this.initWidth = initWidth;
	}
	public String getInitHeight() {
		return initHeight;
	}
	public void setInitHeight(String initHeight) {
		this.initHeight = initHeight;
	}
	public String getImgWidth() {
		return imgWidth;
	}
	public void setImgWidth(String imgWidth) {
		this.imgWidth = imgWidth;
	}
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	public String getCropWidth() {
		return cropWidth;
	}
	public void setCropWidth(String cropWidth) {
		this.cropWidth = cropWidth;
	}
	public String getCropHeight() {
		return cropHeight;
	}
	public void setCropHeight(String cropHeight) {
		this.cropHeight = cropHeight;
	}
	
	
}
