package com.sun3d.why.model;

import java.util.Date;

public class AppAdvertCalendar {
    private String advertId;

    private String advertName;

    private String advertDate;

    private Integer advLink;

    private Integer advLinkType;

    private String advUrl;

    private String advImgUrl;

    private Integer advState;

    private String createBy;

    private String updateBy;

    private Date createTime;

    private Date updateTime;

    public String getAdvertId() {
        return advertId;
    }

    public void setAdvertId(String advertId) {
        this.advertId = advertId;
    }

    public String getAdvertName() {
        return advertName;
    }

    public void setAdvertName(String advertName) {
        this.advertName = advertName;
    }

    public String getAdvertDate() {
        return advertDate;
    }

    public void setAdvertDate(String advertDate) {
        this.advertDate = advertDate;
    }

    public Integer getAdvLink() {
        return advLink;
    }

    public void setAdvLink(Integer advLink) {
        this.advLink = advLink;
    }

    public Integer getAdvLinkType() {
        return advLinkType;
    }

    public void setAdvLinkType(Integer advLinkType) {
        this.advLinkType = advLinkType;
    }

    public String getAdvUrl() {
        return advUrl;
    }

    public void setAdvUrl(String advUrl) {
        this.advUrl = advUrl;
    }

    public String getAdvImgUrl() {
        return advImgUrl;
    }

    public void setAdvImgUrl(String advImgUrl) {
        this.advImgUrl = advImgUrl;
    }

    public Integer getAdvState() {
        return advState;
    }

    public void setAdvState(Integer advState) {
        this.advState = advState;
    }

    public String getCreateBy() {
        return createBy;
    }

    public void setCreateBy(String createBy) {
        this.createBy = createBy;
    }

    public String getUpdateBy() {
        return updateBy;
    }

    public void setUpdateBy(String updateBy) {
        this.updateBy = updateBy;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}